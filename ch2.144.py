from turtle import *
setup(500,400)
bgcolor('light blue')
pensize(1)
pencolor('black')
#circle(25)
goto(0,100)
write('North')
penup()
goto(0,0)
pendown()
goto(0,-100)
write('South')
penup()
goto(0,0)
pendown()
goto(-100,0)
write('West')
penup()
goto(0,0)
pendown()
goto(100,0)
write('East')
penup()
goto(0,-25)
pendown()
circle(25)
hideturtle()
done()